    public void validar(){
        String correo = correo_txt.getText();
        String pass = String.valueOf(password_txt.getPassword());
        if (!"".equals(correo) || !"".equals(pass)){
            login lg = new login();
            loginDAO login = new loginDAO();
            lg = login.log(correo, pass);
            if(lg.getCorreo() != null && lg.getPass() != null) {
                Sistema sis = new Sistema ();
                sis.setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Correo o contrase침a incorrectos");
                correo_txt.setText("");
                password_txt.setText("12345678");
                correo_txt.requestFocus();
            }
        }
        
    }

